function MathOperations(num1,num2,input){
    switch (input){
        case '+': console.log(num1+num2); break;
        case '-': console.log(num1-num2); break;
        case '*': console.log(num1*num2); break;
        case '/': console.log(num1/num2); break;
        case '%': console.log(num1%num2); break;
        case '**': console.log(num1**num2); break;
    }
}